int	ft_is_alpha(char *c)
{
	if (!((*c >= 'A' && *c <= 'Z') || (*c >= 'a' && *c <= 'z')))
	{
		return (0);
	}
	return (1);
}

int	ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if (ft_is_alpha(str) == 0)
		{
			return (0);
		}
		str++;
	}
	return (1);
}
