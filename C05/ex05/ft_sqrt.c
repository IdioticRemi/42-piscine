int	ft_sqrt(int nb)
{
	int	a;
	int	b;

	if (nb <= 1)
		return (nb == 1);
	a = nb / 2;
	if (a)
	{
		b = (a + nb / a) / 2;
		while (b < a)
		{
			a = b;
			b = (a + nb / a) / 2;
		}
		return ((a * a == nb) * a);
	}
	return (0);
}
