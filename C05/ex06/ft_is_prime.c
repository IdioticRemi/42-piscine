int	ft_is_prime(int nb)
{
	unsigned long	i;

	i = 2;
	while (nb > 1 && i * i <= (unsigned) nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return ((nb > 1));
}
